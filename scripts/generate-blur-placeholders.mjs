/**
 * Generates JPEG blurDataURL strings for next/image placeholder="blur".
 * Masters: per-card poster from THAT portrait (not one shared blob).
 *
 * Run: node scripts/generate-blur-placeholders.mjs
 * Writes: lib/blur-placeholders.ts
 */
import sharp from "sharp";
import { writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

/** Same `/masters/…` paths as Masters.tsx MASTER_LIST.image */
const MASTERS_SRC = [
  "/masters/miroslav.png",
  "/masters/oleg.png",
  "/masters/roman.png",
  "/masters/vita.png",
  "/masters/danya.png",
  "/masters/illia.png",
  "/masters/sergiy.png",
  "/masters/mariia.png",
  "/masters/iryna.png",
];

/** Tall portrait thumbnail: enough detail before resize so blur isn’t chunky pixels */
async function makeMasterCardBlur(src) {
  const fsPath = join(root, "public", src.replace(/^\//, ""));
  const buf = await sharp(fsPath)
    .rotate()
    .resize(160, 200, { fit: "cover" })
    .blur(6)
    .resize(80, 100, { fit: "cover" })
    .jpeg({ quality: 82, mozjpeg: true, chromaSubsampling: "4:4:4" })
    .toBuffer();
  console.log(src, buf.length, "bytes");
  return `data:image/jpeg;base64,${buf.toString("base64")}`;
}

async function makeSmallBlur(inputPathRelativeToRoot, label) {
  const buf = await sharp(join(root, inputPathRelativeToRoot))
    .rotate()
    .resize(96, 120, { fit: "cover" })
    .blur(7)
    .resize(48, 48, { fit: "cover" })
    .jpeg({ quality: 78, mozjpeg: true, chromaSubsampling: "4:4:4" })
    .toBuffer();
  console.log(label, buf.length, "bytes");
  return `data:image/jpeg;base64,${buf.toString("base64")}`;
}

const mastersEntries = await Promise.all(
  MASTERS_SRC.map(async (src) => {
    const blur = await makeMasterCardBlur(src);
    return [src, blur];
  })
);

const mastersObj = Object.fromEntries(mastersEntries);
const tattoo = await makeSmallBlur("public/images/tattoo/tattoo-hq-v2.jpg", "tattoo");
const about = await makeSmallBlur("public/images/about/about-new-01.png", "about");

const out = `/**
 * Auto-generated by scripts/generate-blur-placeholders.mjs — do not edit by hand.
 * Masters: separate poster blur per portrait (keys = Image src paths).
 */
export const MASTERS_BLUR_BY_SRC: Record<string, string> = ${JSON.stringify(mastersObj, null, 2)};

/** Fallback if a path is missing (should never happen when lists stay in sync) */
export const MASTERS_BLUR_FALLBACK = MASTERS_BLUR_BY_SRC["/masters/miroslav.png"];

export const TATTOO_IMAGE_BLUR_DATA_URL = ${JSON.stringify(tattoo)};
export const ABOUT_SLIDER_BLUR_DATA_URL = ${JSON.stringify(about)};
`;

await writeFile(join(root, "lib/blur-placeholders.ts"), out, "utf8");
console.log("Wrote lib/blur-placeholders.ts");
